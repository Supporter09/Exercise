export default class Person {
    name;
    age;
    phoneNumber;
    vectorX;
    vectorY;
    value;
    constructor(name, age, phoneNumber, vectorX, vectorY){
        this.name = name;
        this.age = age;
        this.phoneNumber = phoneNumber;
        this.vectorX = vectorX;
        this.vectorY = vectorY;
        this.value = -1;
    }
}